export const featuredBlogs = allBlogs.slice(0, 3)

export function getBlogById(id) {
  return allBlogs.find((blog) => blog.id === id)
}

export function getRelatedBlogs(currentId, category, limit = 3) {
  const sameCategory = allBlogs.filter(
    (blog) => blog.id !== currentId && blog.category === category
  )
  if (sameCategory.length >= limit) return sameCategory.slice(0, limit)
  const usedIds = new Set([currentId, ...sameCategory.map((b) => b.id)])
  const others = allBlogs.filter((blog) => !usedIds.has(blog.id))
  return [...sameCategory, ...others].slice(0, limit)
}
